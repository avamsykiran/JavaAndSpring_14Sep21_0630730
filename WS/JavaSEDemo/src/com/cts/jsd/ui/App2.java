package com.cts.jsd.ui;

public class App2 {

	public static void main(String[] args) {
		System.out.println(Math.pow(2, 5));
		System.out.println(Math.round(3.14));//roundsDown
		System.out.println(Math.round(3.64));//roundsUp
		System.out.println(Math.ceil(3.14));//roundsUp
		System.out.println(Math.ceil(3.64));//roundsUp
		System.out.println(Math.floor(3.14));//roundsDown
		System.out.println(Math.floor(3.64));//roundsDown	
	}

}
